
a = 5
b = 5

# Condiciones: Evalúa si 'a' es mayor, menor o igual que 'b'.
if a > b:
    print(a, "es mayor que", b)
elif a < b:
    print(a,"es menor que",b)
else:
    print(a,"es igual a",b)

# Resultado: Se imprimirá "5 es igual a 5" porque ninguna de las condiciones anteriores es verdadera, pero la última condición (else) sí lo es.



