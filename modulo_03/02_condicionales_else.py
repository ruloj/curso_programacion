a = 3
b = 5

# Condición: Si 'a' es mayor que 'b', ejecuta el primer bloque; de lo contrario, ejecuta el segundo.
if a > b:
    print(a, "es mayor que", b)
else:
    print(b, "es mayor que", a)

# Resultado: Se imprimirá "3 es menor o igual que 5" porque la condición 'a > b' es falsa.



