# Los comentarios se utilizan para dejar notas, explicaciones, o recordatorios.
# Hay dos formas de ponerlos en python:
# Comentario de una sola línea: Se utiliza para añadir comentarios breves. Se inicia con sibolo (#)
# Comentario de múltiples líneas: Se utiliza para comentarios más largos que abarcan varias líneas: Debe ir entre (""" """)
